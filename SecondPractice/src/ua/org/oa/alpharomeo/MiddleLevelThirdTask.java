package ua.org.oa.alpharomeo;

public class MiddleLevelThirdTask {


	public static void main(String[] args) {

		String test = "This is hor*osho!";
		String delimiter = "*";
		String result = "";
		int length = test.length();
		
		for (int i=0; i < length; i++) {
			if ((test.charAt(i) != delimiter.charAt(0)) && (test.charAt(i+1) != delimiter.charAt(0))) {
				result = result.sub;
			}
		}
			
		
		
	}

}
